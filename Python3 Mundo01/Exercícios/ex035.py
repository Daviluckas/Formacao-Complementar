reta1 = float(input('Digite o valor de uma reta númerica: '))
reta2 = float(input('Digite outro: '))
reta3 = float(input('Digite mais um: '))
soma = reta1 + reta2

if soma > reta3:
    print('Essas retas podem sim formar um triângulo!')
else: 
    print('Essas retas não podem formar um triângulo!')
